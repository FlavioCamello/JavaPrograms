package model;

import java.io.Serializable;

/**
 *
 * @author Flavio
 */
public class ModelFuncionario implements Serializable {
    private String nome;
    private int id;

    public ModelFuncionario(int id, String nome) {
        this.nome = nome;
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    @Override
    public String toString() {
        return  id + ","+ nome;
    }
    
    
}
